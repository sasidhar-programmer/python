public class CaseSwitch { 
    public static void main(String[] args) {
        //int a = 3;  
        String b = "sirisha"; 

        switch(b) { 

            case "sasi" : 
                System.out.println("One"); 
                break; 

            case "unknown" :  
                System.out.println("bakar");   
                break ; 

            case "3": 
                System.out.println("three");  
                break ; 

            default : 
                System.out.println("None"); 
                break ; 


        }
    }
    
}