#include<iostream> 
using namespace std; 

int main() { 
    
    int x = 45; 

    switch(x) {  
        
        case 22: 
            cout << "22" << endl; 
            break; 
        
        case 45: 
            cout << "45" << endl; 
            break; 

        default : 
           cout<< "default" << endl; 
    }

    return 0; 
}